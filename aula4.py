# Tipos de números int e float
# int -> número inteiro
# float -> número positivo ou negativo com ponto flutuante
# a função type(é uma classe) mostra o tipo que o Python inferiu ao valor.
# TUDO EM PYTHON É UM OBJETO
# # atalho para comentar mais de uma linha seleciona tudo ctrl+k+ctrl+c
print(1.5)
print(type('SABRINA'))
print(type(25), type(0.6))

# Tipo de dado bool (boolean) ao questionar algo em um programa,
# só existem duas respostas possíveis: sim(True) ou nao(False).
# Existem vários operadores para questionar. Dentre eles o ==,
# que é um operador lógico que questiona se um valor é igual a outro
print(10 == 10, type(10 == 10))
